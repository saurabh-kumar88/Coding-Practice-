package basics;

public class _SwitchCase{

    public static void main(String[] args) {
      char day = '4';

      switch(day){
        case '1':
        System.out.println("1st day");
        break;

        case 2:
        System.out.println("2nd day");
        break;

        case 3:
        System.out.println("3rd day");
        break;

        case '4':
        System.out.println("4th day");
        break;

        default:
        System.out.println("\nIn valid day");
      }


    }


}